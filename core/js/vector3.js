class Vector3 {
    constructor(x = 0, y = 0, z = 0) {
        this.x = x
        this.y = y
        this.z = z
    }

    getX() {
        return this.x
    }

    setX(x) {
        this.x = x
    }

    getY() {
        return this.y
    }

    setY(y) {
        this.y = y
    }

    getZ() {
        return this.z
    }

    setZ(y) {
        this.z = z
    }
}

export default Vector3
